MainWindow::MainWindow(const QString& userName, const QString& userRole, QWidget *parent)
    : QMainWindow(parent), currentActiveBtn(nullptr), employeePage(nullptr), pechePage(nullptr), frigoPage(nullptr), bateauPage(nullptr), livraisonPage(nullptr), quaisPage(nullptr),
      loggedUserName(userName), loggedUserRole(userRole)
{
    setupUi();
    checkMaintenanceAlerts();

    // Initialize Arduino
    int ret = A.connect_arduino();
    switch(ret) {
        case(0): qDebug() << "Arduino connected to:" << A.getarduino_port_name(); break;
        case(1): qDebug() << "Arduino found but failed to connect."; break;
        case(-1): qDebug() << "Arduino not found."; break;
    }

    connect(A.getserial(), &QSerialPort::readyRead, this, &MainWindow::handleSerialData);

    // DEBUG: Force-read timer every second to ensure we don't miss data
    QTimer* forceReadTimer = new QTimer(this);
    connect(forceReadTimer, &QTimer::timeout, this, &MainWindow::handleSerialData);
    forceReadTimer->start(1000);

    // DEBUG: Pulse log to confirm app is alive
    QTimer* pulseTimer = new QTimer(this);
    connect(pulseTimer, &QTimer::timeout, this, [](){
        qDebug() << "--- MONITORING PULSE: App is alive and listening ---";
    });
    pulseTimer->start(2000);

    // Detect which employee table exists
    QSqlQuery testQuery;
    if (testQuery.exec("SELECT * FROM EMPLOYEES WHERE 1=0")) {
        activeEmployeeTable = "EMPLOYEES";
    } else {
        activeEmployeeTable = "EMPLOYEE";
    }
    qDebug() << "Using employee table:" << activeEmployeeTable;

    // Ensure database schema for presence system
    QSqlQuery setupQuery;
    setupQuery.exec(QString("ALTER TABLE %1 ADD RFID_UID VARCHAR2(50)").arg(activeEmployeeTable));
    
    // Create PRESENCE table if it doesn't exist
    setupQuery.exec("CREATE TABLE PRESENCE ("
                    "ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, "
                    "ID_EMPLOYE NUMBER, "
                    "DATE_PRESENCE DATE, "
                    "HEURE_ARRIVEE TIMESTAMP, "
                    "HEURE_DEPART TIMESTAMP)");

    // Initialize global properties
    qApp->setProperty("isDarkMode", isDarkMode);
    qApp->setProperty("isEnglish", isEnglish);
}

