QFrame* MainWindow::createSidebar()
{
    QFrame* sidebar = new QFrame();
    sidebar->setFixedWidth(280);
    sidebar->setStyleSheet(R"(
        QFrame {
            background: qlineargradient(
                x1:0, y1:0, x2:0, y2:1,
                stop:0 #2B5EA6,
                stop:1 #5D9CEC
            );
        }
    )");

    QVBoxLayout* layout = new QVBoxLayout(sidebar);
    layout->setSpacing(0);
    layout->setContentsMargins(0, 0, 0, 0);

    // Logo section
    QFrame* logoFrame = new QFrame();
    logoFrame->setFixedHeight(180); // Increased from 160
    logoFrame->setStyleSheet("background: transparent;");
    QVBoxLayout* logoLayout = new QVBoxLayout(logoFrame);
    logoLayout->setAlignment(Qt::AlignCenter);
    logoLayout->setContentsMargins(0, 20, 20, 20); // Removed left margin to allow more left shift

    QFrame* logoContainer = new QFrame();
    logoContainer->setFixedSize(320, 180); // Increased from 240, 120
    logoContainer->setStyleSheet("background: transparent;");

    QLabel* logoLabel = new QLabel(logoContainer);
    logoLabel->setObjectName("sidebarLogoLabel");
    // Start small and invisible for the "thin air" effect
    logoLabel->setGeometry(80, 65, 150, 50); 
    
    QPixmap logoPix(":/images/images/logo.png");

    if (!logoPix.isNull()) {
        logoLabel->setPixmap(logoPix.scaled(320, 160, Qt::KeepAspectRatio, Qt::SmoothTransformation));
        logoLabel->setAlignment(Qt::AlignCenter);
        qDebug() << "Logo chargé depuis: :/images/images/logo.png";
    } else {
        logoLabel->setText("🚢");
        logoLabel->setStyleSheet(R"(
            QLabel {
                font-size: 60px;
                color: #2C3E50;
            }
        )");
        logoLabel->setAlignment(Qt::AlignCenter);
        qDebug() << "Attention: Logo non trouvé à :/images/images/logo.png - utilisation emoji";
    }

    logoLabel->setStyleSheet("background: transparent;");
    logoLayout->addWidget(logoContainer);
    layout->addWidget(logoFrame);

    // "Appears from Thin Air" Animation Sequence
    QTimer::singleShot(500, [logoLabel]() {
        // 1. Fade In
        QGraphicsOpacityEffect* opacity = new QGraphicsOpacityEffect(logoLabel);
        logoLabel->setGraphicsEffect(opacity);
        QPropertyAnimation* fadeAnim = new QPropertyAnimation(opacity, "opacity");
        fadeAnim->setDuration(1500);
        fadeAnim->setStartValue(0.0);
        fadeAnim->setEndValue(1.0);
        fadeAnim->setEasingCurve(QEasingCurve::InQuad);

        // 2. Scale & Wave (Grow from center)
        QPropertyAnimation* scaleAnim = new QPropertyAnimation(logoLabel, "geometry");
        QRect endRect = QRect(-20, 15, 300, 150); // Shifted even more to the left
        scaleAnim->setDuration(1800);
        scaleAnim->setStartValue(QRect(130, 90, 0, 0)); // Adjusted start center for x=-20
        scaleAnim->setEndValue(endRect);
        scaleAnim->setEasingCurve(QEasingCurve::OutElastic);

        fadeAnim->start(QPropertyAnimation::DeleteWhenStopped);
        scaleAnim->start(QPropertyAnimation::DeleteWhenStopped);

        // 3. Start persistent floating AFTER entrance
        QObject::connect(scaleAnim, &QPropertyAnimation::finished, [logoLabel, endRect]() {
            QPropertyAnimation* floatAnim = new QPropertyAnimation(logoLabel, "pos");
            QPoint startPos = endRect.topLeft();
            floatAnim->setDuration(3500);
            floatAnim->setStartValue(startPos);
            floatAnim->setKeyValueAt(0.5, startPos + QPoint(0, -10));
            floatAnim->setEndValue(startPos);
            floatAnim->setEasingCurve(QEasingCurve::InOutSine);
            floatAnim->setLoopCount(-1);
            floatAnim->start(QPropertyAnimation::DeleteWhenStopped);
        });
    });

    // Navigation
    QFrame* navFrame = new QFrame();
    navFrame->setStyleSheet("background: transparent;");
    QVBoxLayout* navLayout = new QVBoxLayout(navFrame);
    navLayout->setSpacing(8);
    navLayout->setContentsMargins(20, 20, 20, 20);

    dashboardBtn = createNavButton("🏠", "Dashboard", true);
    connect(dashboardBtn, &QPushButton::clicked, this, &MainWindow::onNavigateToDashboard);
    navLayout->addWidget(dashboardBtn);

    QString role = loggedUserRole.trimmed();
    qDebug() << "Current loggedUserRole:" << role;

    // Role-based access control:
    // Admin          → all modules visible
    // Pêcheur        → Pêche only
    // Marin          → Bateau only
    // Technicien     → Bateau only
    // Livreur        → Livraison only
    // RH             → Employés only
    // (empty role)   → all modules (developer/debug)

    bool isAdmin = role.compare("Admin", Qt::CaseInsensitive) == 0 || role.isEmpty();

    auto canAccess = [&](const QStringList& allowedRoles) -> bool {
        if (isAdmin) return true;
        for (const QString& r : allowedRoles) {
            if (role.compare(r, Qt::CaseInsensitive) == 0) return true;
        }
        return false;
    };

    // Bateaux: visible for Admin, Marin, Technicien
    bateauxBtn = createNavButton("⛵", "Bateaux");
    connect(bateauxBtn, &QPushButton::clicked, this, &MainWindow::onNavigateToBateaux);
    navLayout->addWidget(bateauxBtn);
    if (!canAccess({"Marin", "Technicien"})) bateauxBtn->hide();

    // Pêche: visible for Admin, Pêcheur
    pechesBtn = createNavButton("🐟", "Pêche");
    connect(pechesBtn, &QPushButton::clicked, this, &MainWindow::onNavigateToPeches);
    navLayout->addWidget(pechesBtn);
    if (!canAccess({"Pêcheur", "Pecheur"})) pechesBtn->hide();

    // Employés: visible for Admin, RH
    employeesBtn = createNavButton("👥", "Employés");
    connect(employeesBtn, &QPushButton::clicked, this, &MainWindow::onNavigateToEmployees);
    navLayout->addWidget(employeesBtn);
    if (!canAccess({"RH"})) employeesBtn->hide();

    // Frigos: visible for Admin only (no specific role assigned by user)
    QHBoxLayout* frigoRow = new QHBoxLayout();
    frigosBtn = createNavButton("🧊", "Frigos");
    frigoNotifyLabel1 = new QLabel("1️⃣🚨");
    frigoNotifyLabel2 = new QLabel("2️⃣🚨");
    QString notifyStyle = "font-size: 14px; background: transparent; color: #EF4444; font-weight: bold;";
    frigoNotifyLabel1->setStyleSheet(notifyStyle);
    frigoNotifyLabel2->setStyleSheet(notifyStyle);
    frigoNotifyLabel1->hide();
    frigoNotifyLabel2->hide();
    connect(frigosBtn, &QPushButton::clicked, this, &MainWindow::onNavigateToFrigos);
    frigoRow->addWidget(frigosBtn, 1);
    frigoRow->addWidget(frigoNotifyLabel1);
    frigoRow->addWidget(frigoNotifyLabel2);
    navLayout->addLayout(frigoRow);
    if (!isAdmin) {
        frigosBtn->hide();
        frigoNotifyLabel1->hide();
        frigoNotifyLabel2->hide();
    }

    // Livraison: visible for Admin, Livreur
    livraisonBtn = createNavButton("🚚", "Livraison");
    connect(livraisonBtn, &QPushButton::clicked, this, &MainWindow::onNavigateToLivraison);
    navLayout->addWidget(livraisonBtn);
    if (!canAccess({"Livreur"})) livraisonBtn->hide();

    // Quais: visible for Admin only
    quaisBtn = createNavButton("⚓", "Quais");
    connect(quaisBtn, &QPushButton::clicked, this, &MainWindow::onNavigateToQuais);
    navLayout->addWidget(quaisBtn);
    if (!isAdmin) quaisBtn->hide();

    QPushButton* settingsBtn = createNavButton("⚙️", "Paramètres");
    connect(settingsBtn, &QPushButton::clicked, this, &MainWindow::onSettingsClicked);
    navLayout->addWidget(settingsBtn);
    navLayout->addStretch();

    QPushButton* logoutBtn = createNavButton("🚪", "Quitter", false, true);
    connect(logoutBtn, &QPushButton::clicked, this, &MainWindow::onLogout);
    navLayout->addWidget(logoutBtn);

    layout->addWidget(navFrame, 1);

    return sidebar;
}

